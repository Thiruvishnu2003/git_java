public class DataTypes
{
  //Primitive Data Types
  //byte, short, int, long, float, double, boolean, char.
  //Non primitive Data types
  //Classes, Arrays and Interfaces
  public static void main(String args[])
  {
    byte a = 10;
    short b = 20;
    int c = 30;
    long d = 40;
    float e = 50;
    double f = 60;
    boolean g = true;
    char h = 'H';
    String i = "Hello";

    //String is non-primitive. Because, it is a class with methods and properties for manipulating text data.
  }
}
